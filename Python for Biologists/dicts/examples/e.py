dna = "AATGATGAACGAC"


dinucleotides = ['AA','AT','AG','AC',
                'TA','TT','TG','TC',
                'GA','GT','GG','GC',
                'CA','CT','CG','CT']

counts = []
for dinucleotide in dinucleotides:
    count = dna.count(dinucleotide)
    counts.append(counts)


print(counts)


